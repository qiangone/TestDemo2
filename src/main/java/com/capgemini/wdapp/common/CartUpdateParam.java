package com.capgemini.wdapp.common;

public class CartUpdateParam {
	Integer goodsId;
	Integer stockPriceId;
	Integer quantity;
	/**
	 * @return the goodsId
	 */
	public Integer getGoodsId() {
		return goodsId;
	}
	/**
	 * @param goodsId the goodsId to set
	 */
	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}
	/**
	 * @return the stockPriceId
	 */
	public Integer getStockPriceId() {
		return stockPriceId;
	}
	/**
	 * @param stockPriceId the stockPriceId to set
	 */
	public void setStockPriceId(Integer stockPriceId) {
		this.stockPriceId = stockPriceId;
	}
	/**
	 * @return the quantity
	 */
	public Integer getQuantity() {
		return quantity;
	}
	/**
	 * @param quantity the quantity to set
	 */
	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}
	
	
	
}
